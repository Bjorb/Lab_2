

def rectange_rectangle_area(height, width):
    area = 0
    area = height*width
    return area

def rectangle_circumference(height, width):
    circumference = 0
    circumference = 2*height + 2 *width
    return circumference

def third_character_of_string(string):
    if len(string) < 3:
        return False
    else:
        return string[2]



